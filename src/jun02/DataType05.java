//불리언 boolean
/*
 * 저장할 수 있는 값: true 참 on 1 / false 거짓 off 0
 * 
 */
public class DataType05 {
	public static void main(String[] args) {
		boolean check = 1+2==3;//상태
		//계산순서    3  1  2
		
		System.out.println(check);
		System.out.println(!true);//!true: 반전 not
		System.out.println(!check);
		
	}

}

